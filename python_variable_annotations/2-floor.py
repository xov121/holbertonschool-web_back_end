#!/usr/bin/env python3
"""
This module provides a function to find the floor value of a float.
"""
import math


def floor(n: float) -> int:
    """
    Return the floor of a float.

    Args:
    n (float): The float number to find the floor value of.

    Returns:
    int: The floor value of n.
    """
    return math.floor(n)
